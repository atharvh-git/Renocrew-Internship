// Write a java program to convert a string to lowercase.

public class Program1 {
    public static void main(String[] args) {
        String upper_str = "HELLO WORLD";
        // To convert the above string to lowercase, we have
        // a method, toLowercase()
        String lower_str = upper_str.toLowerCase();
        System.err.println(lower_str); // printing the result
    }
}
